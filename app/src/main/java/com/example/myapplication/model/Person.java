package com.example.myapplication.model;

public class Person {
    private String personName;
    private String personEmail;
    private String personImage;
    private String personAddress;
    private String personCountry;
    private String personPhone;

    // Getter and Setter methods for all fields
    public String getPersonName() { return personName; }
    public void setPersonName(String personName) { this.personName = personName; }

    public String getPersonEmail() { return personEmail; }
    public void setPersonEmail(String personEmail) { this.personEmail = personEmail; }

    public String getPersonImage() { return personImage; }
    public void setPersonImage(String personImage) { this.personImage = personImage; }

    public String getPersonAddress() { return personAddress; }
    public void setPersonAddress(String personAddress) { this.personAddress = personAddress; }

    public String getPersonCountry() { return personCountry; }
    public void setPersonCountry(String personCountry) { this.personCountry = personCountry; }

    public String getPersonPhone() { return personPhone; }
    public void setPersonPhone(String personPhone) { this.personPhone = personPhone; }
}


